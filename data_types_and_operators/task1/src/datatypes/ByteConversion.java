package datatypes;

public class ByteConversion {
    static void main(String[] args) {
        int a = 120;

        // byte b = a + 10;  // ошибка компиляции
        // Причина: a + 10 - это выражение типа int (120 + 10 = 130)
        // int не может быть неявно преобразован в byte
        // Требуется явное приведение типа

        byte c = (byte)(a + 10);  // OK: 130 в byte
        // 130 в двоичном: 10000010
        // В byte (со знаком): это -126 (дополнительный код)
        System.out.println("c = (byte)(120 + 10) = " + c);  // -126

        // byte d = a + 1;  // ошибка компиляции
        // По той же причине: a + 1 имеет тип int
        // Даже если результат (121) помещается в byte

        // Правильные варианты:
        byte b1 = (byte)(a + 10);  // явное приведение
        byte d1 = (byte)(a + 1);   // явное приведение
        System.out.println("d1 = (byte)(120 + 1) = " + d1);  // 121
    }
}
