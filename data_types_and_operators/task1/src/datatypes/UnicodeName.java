package datatypes;

public class UnicodeName {
    static void main(String[] args) {
        // В - U+0412, и - U+0438, т - U+0442, а - U+0430, л - U+043B, и - U+0438, я - U+044F
        System.out.println("Моё имя на русском:");
        System.out.println("\u0412\u0438\u0442\u0430\u043B\u0438\u044F");
    }
}
